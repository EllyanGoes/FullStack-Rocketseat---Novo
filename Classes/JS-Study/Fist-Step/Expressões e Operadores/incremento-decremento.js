
let number = 10

//incremento 
number++

console.log(number)

//incremento após 
console.log(number++)
console.log(number)

//incremento antes 
console.log(++number)

//decremento 
//após
console.log(number--)
//antes
console.log(--number)

//incrementar mais de um (colcoar)
number+= 10

console.log(number)

//decrementar mais de um (remover)
number-=20

console.log(number)